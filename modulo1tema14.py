#elimina y almacena elementos de listas con pop()
colores = ['rojo', 'azul', 'verde', 'amarillo', 'marrón', 'lila', 'negro', 'rosa', 'blanco', 'naranja']
#creo una variable y le indico el valor de la lista a eliminar y almacenar
almacena_valor=colores.pop(1)
#imprimo lo que quiero que diga cuando y el resultado de lo anterior (lo separo con coma)
print("el color eliminado y almacenado es:", almacena_valor)


